package ifelseif;

public class If {
    public static void main(String[] args) {
        int a = 0;
        if (a == 0) {
            a = 10;
        } else if (a == 1) {
            System.out.println("芝士雪豹");
        } else if (a == 10) {
            System.out.println("电子烟");
        } else {
            System.out.println("雪豹闭嘴");
        }
    }
}
