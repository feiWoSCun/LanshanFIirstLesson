package math;

public class AnOperation {
    public static void main(String[] args) {
        //异或运算
        int a=127;
        int b=127;
        System.out.println(a^b);


        //位或运算
        int c=127;
        int d=128;
        System.out.println(c|d);

        //与或运算
        int e=15;
        int f=127;
        System.out.println(e&f);

        //位非运算
        int g=1;
        System.out.println(~g);

    }
}
